class ArithmaticOperators
{
      public static void main(String args[])
      {
      int x=9 , y=20;
      System.out.println("x+y="+(x+y));
      System.out.println("x-y="+(x-y));
      System.out.println("x*y="+(x*y));
      System.out.println("x/y="+(x/y));
      System.out.println("x%y="+(x%y));
      }
}